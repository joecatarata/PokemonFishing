package pokemonfishing;


public class Badge {

	public String sBadgeName;
                        
                     public Badge(){
                         sBadgeName = "";
                     }
                    
                     public Badge(String name){
                         sBadgeName = name;
                     }
	public String getsBadgeName() {
		return sBadgeName;
	}

	public void setsBadgeName(String sBadgeName) {
		this.sBadgeName = sBadgeName;
	}
	
}
