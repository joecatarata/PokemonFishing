package pokemonfishing;


public class Shop {

	private Rod Rod;
	private Boat Boat;
	private Tacklebox Tacklebox;
	
	public Rod getRod() {
		return Rod;
	}
	
	public void setRod(Rod rod) {
		Rod = rod;
	}
	
	public Boat getBoat() {
		return Boat;
	}
	
	public void setBoat(Boat boat) {
		Boat = boat;
	}
	
	public Tacklebox getTacklebox() {
		return Tacklebox;
	}
	
	public void setTacklebox(Tacklebox tacklebox) {
		Tacklebox = tacklebox;
	}
	
	
	
}
